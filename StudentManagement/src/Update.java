    private void add_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btActionPerformed

        String name=stu_name.getText();
        String id=user_name.getText();
        String depart=stu_dept.getText();
        String cgpa=stu_cgpa.getText();
        String pass=stu_pass.getText();
        
        if(name.length()>0&&id.length()>0&& depart.length()>0&&cgpa.length()>0&&pass.length()>0) {
        String sql="UPDATE stu_manager SET name='"+name+"',id='"+id+"',dept='"+depart+"',cgpa='"+cgpa+"',pass='"+pass+"'";
            
            dbcon.update(sql);
            JOptionPane.showMessageDialog(null, "Sucessfully updated!");
            close();
            Home h = new Home();
            h.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(null, "Incomplete Entries !");
        }
    }//GEN-LAST:event_add_btActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        close();
        String id=user_name.getText();
        Update s = new Update();
        MainClass m = null;
        if(id.length()>0) {
        String sql="SELECT * from stu_manager where id='"+id+"'";
        m=dbcon.getupdate(sql);
        s.setVisible(true);
        s.stu_name.setText(m.getname());
        s.user_name.setText(m.getid());
        s.stu_cgpa.setText(m.getcgpa());
        s.stu_dept.setText(m.getdept());
        s.stu_pass.setText(m.getpass());
        }
        else{
            JOptionPane.showMessageDialog(null, "Enter Valid Id");
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

